
function gettitleWithValues(values){
	//根据values数组获取完整的表头
	var ttitle = "<thead>";
	for(var v in values){
		ttitle += "<th>"+values[v]+"</th>";
	}
	ttitle += "</thead>";
	return ttitle;
}

function getrowWithValues(values){
	//根据values数组获取完整的表头
	var row = "<tr>";
	for(var v in values){
		row += "<td >"+values[v]+"</td>";
	}
	row += "</tr>";
	return row;
}




